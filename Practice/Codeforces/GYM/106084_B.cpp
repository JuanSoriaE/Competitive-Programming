bool is_prime(int n) {
    if (n <= 1) return 0;
    if (n <= 3) return 1;
    if (n % 2 == 0 || n % 3 == 0) return 0;
    for (int i = 5; i * i <= n; i += 6)
    if (n % i == 0 || n % (i + 2) == 0)
        return 0;
    return 1;
}

bool solve() {
    int a, b; cin >> a >> b;
    return a + 2 == b && is_prime(a) && is_prime(b);
}

int main() {
    ios_base::sync_with_stdio(false);cin.tie(nullptr);cout.tie(nullptr);
    int t; cin >> t;
    while (t--) cout << (solve() ? 'Y' : 'N') << '\n';
    return 0;
}